"""
using the range command, it will generate a squence of number from 0 until to 50.
then it will print it
"""
for x in range(51):
    print(x)

"""
the start of the number is 50 and will go until 0 with an decrements of -1. 
then will print it
"""
for x in range(50, -1, -1):
    print(x)

"""
the start of the number is 30 and will go until 50 with an increments of 1. 
then will print it
"""
for x in range(30, 51):
    print(x)

"""
the start of the number is 50 and will go until 10 with an decrements of -2. 
then will print it
"""
for x in range(50, 9, -2):
    print(x)

"""
the start of the number is 100 and will go until 200 with an increments of 5. 
then will print it
"""
for x in range(100, 201, 5):
    print(x)